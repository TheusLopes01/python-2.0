# Python: Linguagem de Programação
# Tipagem dinâmica e forte
# número sem aspas
print(1+1)
# aspas duplas
print("olá, mundo!") # texto com aspas -> str -> string -> texto
# aspas simples
print('olá')
# escape
print('Matheus \'Lopes\'')
# r
print(r'Matheus \'Lopes\'')
# forma clean de se usar
print('Matheus "Lopes"')

